    private void load_table() {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("No. Telpon");
        model.addColumn("Jenis Barang");
        model.addColumn("Berat");
        model.addColumn("Harga");
        model.addColumn("Total_Harga");
        model.addColumn("Durasi");

        //menampilkan data database kedalam tabel
        try {
            int no = 1;
            String sql = "SELECT no_hp AS no_telp, jenis_barang, Berat, Harga, total_harga, Durasi FROM order_form";
            System.out.println("SQL Query: " + sql);
            java.sql.Connection conn = (ConnectionImpl) function.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{no++, res.getString("no_telp"),
                    res.getString("jenis_barang"), res.getString("Berat"),
                    res.getString("Harga"), res.getString("total_harga"),
                    res.getString("Durasi")});
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

