    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "No row selected. Please select a row to delete.");
            return;
        }

        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            try {
                String no_telp = jTable1.getValueAt(selectedRow, 1).toString(); // Assuming no_telp is in the second column
                java.sql.Connection connection = function.configDB();
                String deleteQuery = "DELETE FROM order_form WHERE no_hp = ?";
                try (var pstmt = connection.prepareStatement(deleteQuery)) {
                    pstmt.setString(1, no_telp);

                    int rowsDeleted = pstmt.executeUpdate();
                    if (rowsDeleted > 0) {
                        System.out.println("Data deleted successfully!");
                        load_table(); // Refresh the table after deletion
                    } else {
                        System.out.println("No data deleted.");
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

